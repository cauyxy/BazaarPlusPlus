import { execFileSync } from 'node:child_process';
import { mkdirSync, readdirSync, rmSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, '..');
const generatedDir = path.join(projectRoot, 'src/lib/generated');
const bindingsDir = path.join(generatedDir, 'bindings');
const barrelPath = path.join(generatedDir, 'commands.ts');
const tauriBindingsDir = path.join(projectRoot, 'src-tauri/bindings');

mkdirSync(generatedDir, { recursive: true });
rmSync(bindingsDir, { recursive: true, force: true });
rmSync(tauriBindingsDir, { recursive: true, force: true });
mkdirSync(bindingsDir, { recursive: true });

execFileSync(
  'cargo',
  ['test', 'export_bindings', '--manifest-path', 'src-tauri/Cargo.toml', '--', '--nocapture'],
  {
    cwd: projectRoot,
    stdio: 'inherit',
    env: {
      ...process.env,
      TS_RS_EXPORT_DIR: '../src/lib/generated/bindings',
      TS_RS_LARGE_INT: 'number'
    }
  }
);

rmSync(tauriBindingsDir, { recursive: true, force: true });

const typeFiles = readdirSync(bindingsDir)
  .filter((name) => name.endsWith('.ts'))
  .sort((left, right) => left.localeCompare(right));

const exports = typeFiles.map((name) => {
  const typeName = path.basename(name, '.ts');
  return `export type { ${typeName} } from './bindings/${typeName}';`;
});

writeFileSync(
  barrelPath,
  `// Generated by scripts/generate-bindings.mjs\n${exports.join('\n')}\n`,
  'utf8'
);
